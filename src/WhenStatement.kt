fun main(args:Array<String>){

    print("Enter Number : ")
    var num = readLine()?.toLong()
    // the when statement in Kotlin is like S
    when(num){

        55525255585->
                println("Zero")

        55858555555->
                println("One")

        100000000000->
                println("Tow")

        2123123112313212->
                println("Hello Ahmad, how it's going?")

        else->
                println("Other Number....")

    }
}